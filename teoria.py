""" VETORES """
# Um vetor é uma estrutura de dados linear e ordenada que armazena elementos de um tipo único
# Cada elemento de um vetor tem índice único que vai de 0 até o tamanho do vetor - 1
# O tamanho de um vetor é fixo e definido durante sua declaração

